function openPage(){
    var x = document.getElementById("search").value;

    if (x === "1"){
        window.open("/1.html");
    }

    if (x === "2"){
        window.open("/2.html");
    }

    if (x === "3"){
        window.open("/3.html");
    }

    if (x === "4"){
        window.open("/4.html");
    }
}